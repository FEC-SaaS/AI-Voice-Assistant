## Unit Economics

### Per-Customer Economics

```
Average Revenue Per User (ARPU): $350/month

Cost Structure per Customer:
├── Vapi.ai costs: $70 (20%)
├── Infrastructure: $15 (4%)
├── Support: $25 (7%)
├── Payment processing: $10 (3%)
└── Total COGS: $120 (34%)

Gross Profit: $230 (66% margin)
```

### Customer Acquisition Cost (CAC)

| Channel | CAC | Payback (months) |
|---------|-----|------------------|
| Organic/SEO | $150 | 0.7 |
| Content Marketing | $200 | 0.9 |
| Paid Ads (Google) | $400 | 1.7 |
| Paid Ads (Facebook) | $350 | 1.5 |
| Partner/Referral | $100 | 0.4 |
| **Blended Average** | **$250** | **1.1** |

### Lifetime Value (LTV)

```
Average customer lifespan: 24 months
Monthly gross profit: $230

LTV = $230 × 24 = $5,520

LTV:CAC Ratio = $5,520 / $250 = 22:1

(Excellent - target is >3:1)
```

