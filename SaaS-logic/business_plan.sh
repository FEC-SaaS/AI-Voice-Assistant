## Sales Strategy

### Self-Serve (60% of customers)

```
Website → Free Trial → Onboarding → Conversion

- 14-day free trial (no credit card)
- Automated email sequences
- In-app guidance and tutorials
- Self-serve upgrade path
```

### Sales-Assisted (35% of customers)

```
Demo Request → Discovery Call → Proposal → Close

- Inbound demo requests
- 15-minute discovery calls
- Custom proposals for Business tier
- 2-week average sales cycle
```

### Enterprise (5% of customers)

```
Outbound → Multiple Calls → Pilot → Contract

- Dedicated AE for $5K+/month deals
- Custom pilots and POCs
- 60-90 day sales cycles
- Annual contracts with SLAs
```

